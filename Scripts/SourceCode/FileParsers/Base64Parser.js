/**  EncodeFile using Base64 */

function encode(inputPath, outputPath, isWebSafe, bufferSize)
{
Base64Parser.EncodeFile(inputPath, outputPath, isWebSafe, bufferSize);
}

/** DecodeFile using Base64. */

function decode(inputPath, outputPath, isWebSafe, bufferSize)
{
Base64Parser.DecodeFile(inputPath, outputPath, isWebSafe, bufferSize);
}